/** 노선 배열을 공식 순서(1호선→2호선→…→지방)로 정렬 */
export function sortLines(lines: string[]): string[] {
  return [...lines].sort((a, b) => (LINE_ORDER[a] ?? 99) - (LINE_ORDER[b] ?? 99));
}

